public class UnaryOperator {
    public static void main(String args[]){
        int a=10;
        int b=13;
        int c=11;
        boolean d = false;
        //incremental
        a++; //a=a+1;
        //decremental
        b--; //b=b-1;
        System.out.println("Incremental "+a);
        System.out.println("Decremental "+b);
        System.out.println(~c);
        System.out.println(!d);


    }
}
